 class Main {
    public static void main(String[] args) {
        int a = 5;
        short b = 2;
        long c = 180;
        byte d = 127;

        char e = 'A';
        boolean f = true;

        float g = 3.14f;
        double h = 3.14;

        System.out.println("int value = " + a);
        System.out.println("short value = " + b);
        System.out.println("long value = " + c);
        System.out.println("byte value = " + d);
        System.out.println("char value = " + e);
        System.out.println("boolean value = " + f);
        System.out.println("float value = " + g);
        System.out.println("double value = " + h);


    }
}
